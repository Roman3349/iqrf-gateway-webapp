declare(strict_types = 1);

namespace App\GatewayModule\Models;

use App\CoreModule\Models\CommandManager;

/**
 * Device tree board manager
 */
class DeviceTreeBoardInfoManager implements IBoardInfoManager {

	/**
	 * @var CommandManager Command manager
	 */
	private $commandManager;

	/**
	 * Constructor
	 * @param CommandManager $commandManager Command manager
	 */
	public function __construct(CommandManager $commandManager) {
		$this->commandManager = $commandManager;
	}

	/**
	 * Get board's name from device tree
	 * @return string|null Board's name
	 */
	public function getName(): ?string {
		$deviceTree = $this->commandManager->send('cat /proc/device-tree/model', true);
		if ($deviceTree !== '') {
			return $deviceTree;
		}
		return null;
	}

}
